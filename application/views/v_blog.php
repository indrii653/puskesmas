                                    2. Tahap Kedua Imunisasi Anak Nasional Tahap Kedua dilaksanakan pada bulan Agustus 2022. Wilayah sasaran dari program imunisasi tahap kedua ini adalah Pulau Jawa dan Provinsi Bali. </p>
